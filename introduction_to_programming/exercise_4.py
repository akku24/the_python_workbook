



### In this program we have to  calculate the area in sq.feet,
### After calculation we have to convert it into acre by dividing area by 43560.







l = float(input("Enter the length of the field in feets >> ")) # Taken l to represent length of field.
b = float(input("Enter the width of the field in feets >> ")) # Taken b to represent width of field.
area = l * b / 43560
print("The area of field in acre is %.3f"%area)


