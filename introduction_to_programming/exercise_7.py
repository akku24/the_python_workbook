


### In this program we have to calculate the sum till nth number and then print the result.

n = int(input("Enter the number to find out sum till that number >> ")) # Asks the user to give nth number for calculation.
sum = (n * (n + 1)) / 2						       # It is the formula for calculating the sum till nth number.
print("The sum of first n number is >> ",sum)			       # Priints the output of sum we calculated above.
