

# The program is to calculate the area of the given room. 
# We have to ask the user of length and width and tell them  area of their room.
# the units can be as per our comfort meters or feets. 





l = float(input("Enter the length of your room >>")) # Here I take l to represent the length of room.
b = float(input("Enter the width of your room >>"))  # Here I take b to represent the breadth of room.
area = l * b                                         # Calculation for area.
print("Area of your room is %.2f"%area)              # Printing the output to the user.
					         # Here we are restricting our answer to 2 decimal places

 
